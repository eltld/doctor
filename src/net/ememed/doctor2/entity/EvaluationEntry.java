package net.ememed.doctor2.entity;

public class EvaluationEntry {
	private String ID;
	private String DSRATTITUDE;
	private String DSRTIME;
	private String RATECONTENT;
	private String STYPE;
	private String USERNAME;
	private String USERAVATAR;
	
	private String TITLE;
	
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getDSRATTITUDE() {
		return DSRATTITUDE;
	}
	public void setDSRATTITUDE(String dSRATTITUDE) {
		DSRATTITUDE = dSRATTITUDE;
	}
	public String getDSRTIME() {
		return DSRTIME;
	}
	public void setDSRTIME(String dSRTIME) {
		DSRTIME = dSRTIME;
	}
	public String getRATECONTENT() {
		return RATECONTENT;
	}
	public void setRATECONTENT(String rATECONTENT) {
		RATECONTENT = rATECONTENT;
	}
	public String getSTYPE() {
		return STYPE;
	}
	public void setSTYPE(String sTYPE) {
		STYPE = sTYPE;
	}
	public String getUSERNAME() {
		return USERNAME;
	}
	public void setUSERNAME(String uSERNAME) {
		USERNAME = uSERNAME;
	}
	public String getUSERAVATAR() {
		return USERAVATAR;
	}
	public void setUSERAVATAR(String uSERAVATAR) {
		USERAVATAR = uSERAVATAR;
	}
	public String getTITLE() {
		return TITLE;
	}
	public void setTITLE(String tITLE) {
		TITLE = tITLE;
	}
	
}
